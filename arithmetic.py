# arithmetic operators are used to undertake maths operators
# e.g. +-/*
# % modulus
# // floor division
# ** exponent

a:int = 20
b:int = 6

print(a+b)
print(a-b)
print(a*b)
print(a/b)

# modulus - gives the reminder of a division of left operand by the right
print(a%b)

# floor division - will return a whole number 

print(a//b)

# exponent - left operand raised to the power of the right
print(a**3)