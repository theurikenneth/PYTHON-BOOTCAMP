""" COMPARISON OPERATORS
< - less than
> - greater than
== equal to
>= greater than or equal to
<= less than or equal to
!= not equal
"""
print(10>20) #False
print(2>1) #True
print(10==11) #False
print(10==5*2) #True
print(18>=21) #False
print(18<=52) #True
print(18<=18) #True
print(27!=34) #True
